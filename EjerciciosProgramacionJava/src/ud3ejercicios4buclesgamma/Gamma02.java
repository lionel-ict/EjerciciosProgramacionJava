
public class Gamma02 {

    public static void main(String[] args) {

        // Para cada línea i de 1 a 9
        for (int i = 1; i <= 9; i++) {

            // Mostramos i diez veces
            for (int j = 1; j <= 10; j++) {
                System.out.print(i);
            }

            // nueva línea
            System.out.println("");

        }
    }
}
