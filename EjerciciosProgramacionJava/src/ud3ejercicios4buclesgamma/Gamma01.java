
public class Gamma01 {

    public static void main(String[] args) {

        // Para cada línea i de 1 a 9
        for (int i = 1; i <= 9; i++) {

            // Mostramos los números del 0 al 9
            for (int j = 0; j <= 9; j++) {
                System.out.print(j);
            }

            // nueva línea
            System.out.println("");

        }
    }
}
